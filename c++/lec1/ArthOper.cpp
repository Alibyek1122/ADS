#include <iostream>
using namespace std;

int main() {

    int a = 10 , b = 3;

    int plus,minus,mult,div,mod;






    plus = a + b; // 'hosu'
    minus = a - b; // alu
    mult = a * b; // kobeitu
    div = a / b; // 'bolu'
    mod = a % b; // remainder of division  'haldig'

    cout<<plus<<endl;
    cout<<minus<<endl;
    cout<<mult<<endl;
    cout<<div<<endl;
    cout<<mod<<endl;



    return 0;
}
